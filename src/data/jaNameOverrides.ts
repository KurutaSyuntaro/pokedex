export function normalizeName(name: string): string {
  return String(name || "")
    .toLowerCase()
    .replace(/\./g, "")
    .replace(/\s+/g, "")
    .replace(/-/g, "_");
}

export function lookupJaOverride(enName: string): string {
  const key = normalizeName(enName);
  return JA_NAME_OVERRIDES[key] || enName;
}
